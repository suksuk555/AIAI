def _encode_with_saved_encoders(df, cols, label_encoders):
    df = df.copy()
    for c in cols:
        le = label_encoders.get(c)
        if le is None:
            continue
        # map unseen labels -> -1 ‡πÅ‡∏•‡πâ‡∏ß‡∏Å‡∏£‡∏≠‡∏á‡∏≠‡∏≠‡∏Å‡∏†‡∏≤‡∏¢‡∏´‡∏•‡∏±‡∏á
        df[c] = df[c].map(lambda x: x if x in le.classes_ else None)
    # ‡∏Å‡∏£‡∏≠‡∏á‡∏Ñ‡πà‡∏≤‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡πá‡∏ô None (unseen) ‡∏≠‡∏≠‡∏Å
    for c in cols:
        if c in df.columns:
            df = df[df[c].notna()]
    # ‡πÅ‡∏õ‡∏•‡∏á‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏î‡πâ‡∏ß‡∏¢ encoders ‡∏ó‡∏µ‡πà‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÑ‡∏ß‡πâ
    for c in cols:
        le = label_encoders.get(c)
        if le is None:
            continue
        df[c] = le.transform(df[c])
    return df

